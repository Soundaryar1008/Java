package ControlStatement;

public class ifelseifexample {
    public static void main(String[] args) {
        int n=0;
        if(n>0){
            System.out.println("positive n");
        }
        else if(n<0){
            System.out.println("negative");
        }
        else{
            System.out.println("neither negative nor positive");
        }
    }
}
